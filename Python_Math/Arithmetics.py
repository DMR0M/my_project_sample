class Arithmetic:
    @staticmethod
    def add(n, x):
        return n + x

    @staticmethod
    def subtract(n, x):
        return n - x

    @staticmethod
    def multiply(n, x):
        return n * x

    @staticmethod
    def divide(n, x):
        return n / x

    @staticmethod
    def square(n):
        return n ** 2

    @staticmethod
    def cube(n):
        return n ** 3


